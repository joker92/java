package Anagrafe;

public class Coppia {

    private Cane cane;
    private Padrone padrone;

    public Coppia(Cane cane, Padrone padrone) {
        this.cane = cane;
        this.padrone = padrone;
    }

    public Cane getCane() {
        return cane;
    }

    public Padrone getPadrone() {
        return padrone;
    }

}
